'''
sequence（序列）是python中最基本的一种数据结构
数据结构是指计算机中数据存储的方式
序列用于保存一组有序的数据，所有的数据在序列中都要一个唯一的位置（索引）
并且序列中的数据会按照添加的顺序来分配索引
序列的分类
    -可变序列（序列中的元素可以改变）
        >list
    -不可变序列(序列中的元素不可以改变)
        >str
        >tuple
对列表的操作同样适用于其他序列,不可变序列无法通过索引修改，可以通过list()函数将其他的序列转换为list
'''
